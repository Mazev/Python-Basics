produkt = input()

if produkt == 'banana' or produkt == 'apple' or produkt == 'kiwi' or produkt == 'lemon' or produkt == 'grapes'\
        or produkt == 'cherry':
    print('fruit')
elif produkt == 'tomato' or produkt == 'cucumber' or produkt == 'pepper' or produkt == 'carrot':
    print('vegetable')
else:
    print('unknown')